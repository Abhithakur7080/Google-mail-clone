export const isSpam = (data) => {
  return spamKeywords.some(
    (keyword) =>
      data.receiver.toLowerCase().includes(keyword) ||
      data.subject.toLowerCase().includes(keyword) ||
      data.content.toLowerCase().includes(keyword)
  );
};
