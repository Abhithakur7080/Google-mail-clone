export const sortMail = (arr = []) => {
  const sortedMail = arr.sort((a, b) => {
    const aTimestamp = a.createdAt ? a.createdAt.seconds : 0;
    const bTimestamp = b.createdAt ? b.createdAt.seconds : 0;
    return bTimestamp - aTimestamp;
  });
  return sortedMail;
};
